package ar.edu.unju.escmi.tp2.ejercicio7;

public class Ejercicio7 {

    public static void main(String[] args) {
        
        int a = 30;
        double b = 1.5;

        // Muestra el valor de 'a' y luego lo incrementa.
        System.out.println(a++);

        // Muestra el valor actual de 'a'.
        System.out.println(a);

        // Decrementa el valor de 'a' y luego lo muestra.
        System.out.println(--a);

        // Incrementa el valor de 'b' y luego lo muestra.
        System.out.println(++b);

        // Muestra el valor de 'b' y luego lo decrementa.
        System.out.println(b--);

        // Muestra el valor actual de 'b'.
        System.out.println(b);
    }
}